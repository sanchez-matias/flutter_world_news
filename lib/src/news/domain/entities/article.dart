class Article {
  final String? author;
  final String? content;
  final String? description;
  final String? publishedAt;
  final String? sourceName;
  final String? title;
  final String? url;
  final String? urlToImage;

  const Article({
    this.author,
    this.content,
    this.description,
    this.publishedAt,
    this.sourceName,
    this.title,
    this.url,
    this.urlToImage,
  });
}
